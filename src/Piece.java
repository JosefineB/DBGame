
public class Piece {
    int id;
    int x;
    int y;
    int z;
    int width;
    int height;
    int depth;

    public Piece(int id, int x, int y, int z, int width, int height, int depth){
        this.id = id;
        this.x = x;
        this.y = y;
        this.z = z;
        this.width = width;
        this.height = height;
        this.width= width;
        this.depth = depth;
    }
}
